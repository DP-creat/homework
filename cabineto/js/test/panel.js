window.togglePanel = (name, options = {}) => {
    const { 
        isExclusive = false, 
        resetScroll = false, 
        checkSelection = false 
    } = options;

    const target = document.querySelector(`.panel.${name}`);
    if (!target) return;

    // 1. Проверка выделения текста (твоя фишка из логов)
    if (checkSelection) {
        const selection = window.getSelection().toString();
        if (target.classList.contains('active') && selection.length > 0) {
            return; 
        }
    }

    // 2. Режим эксклюзивности (аккордеон)
    if (isExclusive) {
        document.querySelectorAll('.panel.active').forEach(p => {
            if (p !== target) p.classList.remove('active');
        });
    }

    // 3. Переключаем состояние
    const isActive = target.classList.toggle('active');

    // 4. Специфическая логика (прокрутка лога в начало)
    if (isActive && resetScroll) {
        target.scrollTop = 0;
    }

    // 5. Блокировка скролла всей страницы
    const anyActive = document.querySelector('.panel.active');
    document.body.style.overflow = anyActive ? 'hidden' : '';
};
// Объект options вместо кучи аргументов:Вместо togglePanel('log', 1, 1, 1), где непонятно, что значат цифры, ты пишешь:togglePanel('log', { isExclusive: true, resetScroll: true }). Это именно то, как передаются настройки в компоненты React.Защита выделения (checkSelection):Мы интегрировали твой код из логов. Теперь одна и та же функция может и просто открывать панель, и «умно» закрывать лог, не мешая копированию текста.Логика resetScroll:Для обычных панелей это не нужно, а для логов — критично. Теперь это настраиваемый параметр.Как это будет в React:Там мы уберем document.querySelector и classList.toggle. Вместо этого мы будем передавать эти же options в компонент. Например:<Panel isOpen={state} resetScroll={true} />.Как теперь вызывать для разных нужд:Для обычной панели: togglePanel('settings')Для лога (с твоими фишками): togglePanel('log', { isExclusive: true, resetScroll: true, checkSelection: true })Для мануала (эксклюзивно): togglePanel('manual', { isExclusive: true })

// 3. Как читать параметры в onclickКогда ты пишешь togglePanel('имя', { опции }), вот что делает каждая настройка:isExclusive: true — если открыта одна панель, а ты нажимаешь на другую, первая закроется автоматически (как аккордеон).resetScroll: true — полезно для длинных логов. При каждом открытии панель будет «откручена» в самый верх.checkSelection: true — твоя фишка. Если ты выделил текст в логе, клик не закроет панель. Попробуй выделить слово и кликнуть — панель останется. Если ничего не выделено — закроется.Почему это удобно для перехода на Реакт:В HTML ты сейчас используешь атрибут onclick. В Реакте ты просто перенесешь это в onClick={() => togglePanel(...)}. Сама логика функции останется почти такой же, только вместо поиска элементов через document.querySelector, Реакт будет управлять ими через "стейты" (состояния).

